import { GUARDRAIL_ACTION } from '@shared-types/guardrail.contract';
import type { FactRule } from '../output-rule.types';

/**
 * 歧视性筛选条件外露规则。
 *
 * 职责：
 * - 管“候选人能从回复里直接看出自己被户籍/籍贯/民族/专业筛掉”的场景；
 * - 这些限制即使是真实业务筛选条件，也只能留在内部决策链路里，不能作为对外拒绝理由；
 * - 命中后用 block，因为一旦发送给候选人，后续很难通过补充解释消除聊天证据风险。
 *
 * 不负责：
 * - 不判断候选人是否真的满足岗位要求；这里不做资格计算，只看 reply 是否把敏感门槛说出来；
 * - 不覆盖保险/社保、岗位品牌名、岗位事实编造，它们分别在独立领域规则里维护；
 * - 性别与年龄要求按业务口径属于岗位公开信息（岗位卡本来就写"要求：18-40岁，仅限男"），
 *   不在歧视外露范畴（2026-07-03 上线首日运营裁决，原 gender_direct_reject /
 *   age_requirement_disclosure 两条规则已删除）。
 *
 * 维护边界：
 * - 新增词时优先围绕“拒绝/限制 + 敏感属性”的组合补充，避免把中性确认句误杀；
 * - 如果只是“请问您的籍贯”“你学的什么专业”这类收资话术，不应放到这里拦，
 *   除非它明确暴露筛选阈值或拒绝理由（如反问式“专业不是新媒体或食品相关的吧”，
 *   把排除条件塞进问句里，2026-07-06 运营反馈要求与籍贯/民族同样处理）。
 */

const ETHNIC_TERM = '少数民族|维吾尔族|哈萨克族|蒙古族|朝鲜族|土家族|[汉回藏满苗彝壮侗瑶白傣黎]族';

const DISCRIMINATORY_LEAK_PATTERN = new RegExp(
  [
    // 明确拒收/不招某类户籍、民族或地域身份。
    `(?:不要|不收|不招|不接受|不考虑|谢绝|拒绝|排除)[^。！？?\\n]{0,10}?(?:户籍|户口|籍(?!贯)|${ETHNIC_TERM})`,
    // “仅限/只招”类正向限制同样会暴露筛选条件；排除“籍贯”里的“籍”误命中。
    `(?<!不)(?:仅限|只限|只招|只收|限)[^。！？?\\n]{0,8}?(?:户籍|户口|本地人|籍(?!贯)|${ETHNIC_TERM})`,
    // 拒收/圈定某类专业背景。间隔禁跨逗号（避免“不要紧，我们有专业带教”误伤），
    // 并排除“专业培训/带教”等形容词用法。
    '(?:不要|不收|不招|不接受|不考虑|谢绝|拒绝|排除)[^，。；！？?\\n]{0,10}?专业(?!培训|带教|团队|课程|指导|老师)',
    '(?<!不)(?:仅限|只限|只招|只收|限)[^，。；！？?\\n]{0,8}?专业(?!培训|带教|团队|课程|指导|老师)',
    // 把“户籍/民族/地域/专业要求”作为原因说出来，即使没有点名具体群体也应拦。
    '(?<!没有)(?<!无)(?:户籍|籍贯|民族|地域|专业)(?:要求|限制)(?!不限)',
    // “不符/不匹配”是常见的拒绝解释，需要覆盖。
    '(?:户籍|籍贯|民族|专业)[^。！？?\\n]{0,6}?不(?:符|匹配)',
    // 反问式暴露专业排除条件：“专业不是新媒体或食品相关的吧”。
    // 两个方向都要求出现“相关/类/专业”锚点，避免误杀“我们很专业，不是中介”这类中性句。
    '专业(?:不是|不能是|不可以是|别是)[^。！？?\\n]{0,15}?(?:相关|类|专业)',
    '(?:不是|不会是|别是)[^。！？?\\n]{0,10}?(?:相关|类)专业',
    // 把岗位后台的筛选题 label 原样转述：“专业（非新媒、食品）”。
    '专业\\s*[（(][^）)]*?非',
  ].join('|'),
);

export const DISCRIMINATION_LEAK_RULES: FactRule[] = [
  {
    ruleId: 'discriminatory_screening_leak',
    label:
      '回复疑似向候选人透露户籍/籍贯/民族/专业等歧视性筛选条件（敏感门槛 🔒 仅供内部筛选，外露涉地域/民族/专业歧视纠纷风险）',
    keywords: DISCRIMINATORY_LEAK_PATTERN,
    requiredToolPredicate: () => false,
    action: GUARDRAIL_ACTION.BLOCK,
  },
];
