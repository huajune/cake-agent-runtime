import { GUARDRAIL_ACTION } from '@shared-types/guardrail.contract';
import type { FactRule } from '../output-rule.types';

/**
 * 歧视性筛选条件外露规则。
 *
 * 职责：
 * - 管“候选人能从回复里直接看出自己被户籍/籍贯/民族/性别/年龄筛掉”的场景；
 * - 这些限制即使是真实业务筛选条件，也只能留在内部决策链路里，不能作为对外拒绝理由；
 * - 命中后用 block，因为一旦发送给候选人，后续很难通过补充解释消除聊天证据风险。
 *
 * 不负责：
 * - 不判断候选人是否真的满足岗位要求；这里不做资格计算，只看 reply 是否把敏感门槛说出来；
 * - 不覆盖保险/社保、岗位品牌名、岗位事实编造，它们分别在独立领域规则里维护。
 *
 * 维护边界：
 * - 新增词时优先围绕“拒绝/限制 + 敏感属性”的组合补充，避免把中性确认句误杀；
 * - 如果只是“请问您的年龄/性别/籍贯”这类收资话术，不应放到这里拦，除非它明确暴露筛选阈值或拒绝理由。
 */

const ETHNIC_TERM = '少数民族|维吾尔族|哈萨克族|蒙古族|朝鲜族|土家族|[汉回藏满苗彝壮侗瑶白傣黎]族';

const DISCRIMINATORY_LEAK_PATTERN = new RegExp(
  [
    // 明确拒收/不招某类户籍、民族或地域身份。
    `(?:不要|不收|不招|不接受|不考虑|谢绝|拒绝|排除)[^。！？?\\n]{0,10}?(?:户籍|户口|籍(?!贯)|${ETHNIC_TERM})`,
    // “仅限/只招”类正向限制同样会暴露筛选条件；排除“籍贯”里的“籍”误命中。
    `(?<!不)(?:仅限|只限|只招|只收|限)[^。！？?\\n]{0,8}?(?:户籍|户口|本地人|籍(?!贯)|${ETHNIC_TERM})`,
    // 把“户籍/民族/地域要求”作为原因说出来，即使没有点名具体群体也应拦。
    '(?<!没有)(?<!无)(?:户籍|籍贯|民族|地域)(?:要求|限制)(?!不限)',
    // “不符/不匹配”是常见的拒绝解释，需要覆盖。
    '(?:户籍|籍贯|民族)[^。！？?\\n]{0,6}?不(?:符|匹配)',
  ].join('|'),
);

const GENDER_DIRECT_REJECT_PATTERN = new RegExp(
  [
    // 明确“不招男/女”。
    '(?:不招|不要|不收|不接受|不考虑)[^。！？\\n]{0,6}(?:男性|女性|男生|女生|男的|女的)',
    // “只招男/女”与直接拒绝风险等价。
    '(?<!不)(?:只招|仅招|只收|只要|仅限|仅要)[^。！？\\n]{0,6}(?:男性|女性|男生|女生)',
    // 岗位/门店口径中暴露性别偏好。
    '(?:这(?:个)?岗位|我们(?:这里)?)[^。！？\\n]{0,10}只(?:招|要|收)[^。！？\\n]{0,6}(?:男性|女性|男生|女生|男的|女的)',
    // 直接对候选人说“你是 X 性别所以不合适”。
    '(?:您|你)[^。！？\\n]{0,8}(?:男性|女性|男生|女生|男的|女的)[^。！？\\n]{0,15}(?:不(?:合适|符合|适合)|没法|无法|不行)',
  ].join('|'),
);

const AGE_REQUIREMENT_DISCLOSURE_PATTERN = new RegExp(
  [
    // “超龄/年龄超出”直接把年龄筛选原因外露。
    '(?:您|你)?(?:的年龄)?超龄了?|年龄.*超(?:出|了|过)|超(?:出|过)了?.*年龄',
    // 把岗位年龄上下限数值发给候选人。
    '(?:岗位|公司|门店|这里|这边)[^。！？\\n]{0,12}(?:要求|限制|需要)[^。！？\\n]{0,12}\\d{1,2}[-~到至]\\d{1,2}岁',
    // “年龄要求/限制/门槛 X 岁”属于同类外露。
    '(?:年龄|岁数)(?:要求|限制|门槛)[^。！？\\n]{0,8}\\d{1,2}岁',
    // “只招 18-35 岁”这类短句。
    '只(?:招|收|要)[^。！？\\n]{0,8}\\d{1,2}[-~到至]\\d{1,2}岁',
  ].join('|'),
);

export const DISCRIMINATION_LEAK_RULES: FactRule[] = [
  {
    ruleId: 'discriminatory_screening_leak',
    label:
      '回复疑似向候选人透露户籍/籍贯/民族等歧视性筛选条件（敏感门槛 🔒 仅供内部筛选，外露涉地域/民族歧视纠纷风险）',
    keywords: DISCRIMINATORY_LEAK_PATTERN,
    requiredToolPredicate: () => false,
    action: GUARDRAIL_ACTION.BLOCK,
  },
  {
    ruleId: 'gender_direct_reject',
    label:
      '回复疑似以性别为由直接拒绝或透露性别筛选条件（性别筛选 🔒 仅供内部，外露涉性别歧视纠纷风险）',
    keywords: GENDER_DIRECT_REJECT_PATTERN,
    requiredToolPredicate: () => false,
    action: GUARDRAIL_ACTION.BLOCK,
  },
  {
    ruleId: 'age_requirement_disclosure',
    label:
      '回复向候选人透露岗位年龄要求数值或告知超龄（年龄要求 🔒 仅供内部筛选，超龄时需用排班/距离等中性理由转推其他岗位，禁止明说年龄原因）',
    keywords: AGE_REQUIREMENT_DISCLOSURE_PATTERN,
    requiredToolPredicate: () => false,
    action: GUARDRAIL_ACTION.BLOCK,
  },
];
