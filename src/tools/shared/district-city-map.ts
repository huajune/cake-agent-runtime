/**
 * 在候选人原文中扫描无歧义区级地名，返回它们映射到的城市集合（裸名，
 * 与 normalizeCity 输出同口径）。出处判定不做意图判定（与 user_text
 * 档同限制）：候选人提到他人城市/否定语境仍会被计入。
 */
export function inferCitiesFromDistrictMentions(userTexts: readonly string[]): Set<string> {
  const cities = new Set<string>();
  for (const text of userTexts) {
    if (!text) continue;
    for (const [district, city] of UNAMBIGUOUS_DISTRICT_CITY_ENTRIES) {
      if (text.includes(district)) {
        cities.add(city);
      }
    }
  }
  return cities;
}
